package fundamentos;

public class AreaCircunferencia {

    public static void main(String[] args) {
        double raio = 3.4; //Alocação de espaço em memória, informando o valor da variável raio
        final double PI = 3.14159; //valor constante, não é uma variável

        double area = PI * raio * raio;

        System.out.println(area);




    }
}
