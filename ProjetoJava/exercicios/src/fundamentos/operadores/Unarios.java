package fundamentos.operadores;

public class Unarios {
    public static void main(String[] args) {

        int a = 1;
        int b = a;

        a++; //incrementar, ou seja a = a + 1
        a--; //decrementar, ou seja a = a - 1

        ++a; //b = b + 1
        --b; //b = b - 1;

        System.out.println(a);
        System.out.println(b);
    }
}
